    /**
     * Constructor for PrintReportForm which takes a TeamManager so teams can be read from a file
     * @param Manager The team manager that will manage the teams
     */
    public PrintReportForm(TeamManager Manager)
    {
        this.manager = Manager;
        initComponents();
         try
        {
            String ReadText;
            File f = new File("Matches.txt");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            while((ReadText = br.readLine()) != null)
            {
                String replace = ReadText.replace(',', ' ');
                this.txtReport.setText(this.txtReport.getText() + replace + "\n");
            }
            br.close();
            
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "An Error occured while reading the data from a file");
        }
    }

